package Fundamentos;

public class NãoPrimitivo {
    public static void main(String[] args) {
        int x = 2;
        // O Integer é uma class, ou seja, posso fazer vários tipos de operações com o número, diferentemente de usar um tipo primitivo normal.
        Integer y = 4;
        // Neste caso, estou transformando o número 4 para uma string "4". Você pode fazer muitas outras operações usando o Integer.
        y.toString();
    }
}
