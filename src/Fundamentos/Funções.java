package Fundamentos;

public class Funções {

    public static void main(String[] args){
        sum(10, 20);
    }

    // static void -> Definição padrão
    // sum -> Nome da função
    // () -> Parâmetros
    // {} -> Bloco a ser executado
    static void sum(int x, int y){
        System.out.println(x + y);
    }

}


