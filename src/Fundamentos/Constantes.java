package Fundamentos;

public class Constantes {
    public static void main(String[] args) {
        // VARIÁVEIS IMUTÁVEIS (CONSTANTES)

        // Como o PI é uma constante, adicionamos a palavra "final" antes de declarar a variável, fazendo com que não se possa mudar o valor dela em outra parte do código
        final double PI = 3.14159;

        // Isso traz um erro de compilação, pois é uma constante:
        // PI = 10;
    }
}
